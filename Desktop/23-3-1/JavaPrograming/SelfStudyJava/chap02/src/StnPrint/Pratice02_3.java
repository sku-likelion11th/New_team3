package StnPrint;

public class Pratice02_3 {

	public static void main(String[] args) {
		int x = 300;
		int y = 300;		// 변수 선언
		System.out.println("x = " + x);
		System.out.println("y = " + y);		// x, y에 할당된 변수 출력
		System.out.print("x를 42로 나눈몫은 " + (x/42) + ", 나머지는 " + (x%42) + "입니다.\n");
		System.out.print("y를 38로 나눈몫은 " + (y/38) + ", 나머지는 " + (y%38) + "입니다.");
		// x, y를 연산하여 결과를 모니터에 출력
	}
}
