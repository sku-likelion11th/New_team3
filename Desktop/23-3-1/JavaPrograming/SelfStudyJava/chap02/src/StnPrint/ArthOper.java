package StnPrint;

public class ArthOper {
	public static void main(String[] args) {
		int a, b;
		double f,g;
		a = 20; b= 15;
		f = 18.4; g = 4.5;
		System.out.println("a + b : " + (a+b));
		System.out.println("a - b : " + (a-b));
		System.out.println("a * b : " + (a*b));
		System.out.println("a / b : " + (a/b));
		System.out.println("a / g : " + (a/g));
		System.out.println("a % b : " + (a%b));
	}
}
